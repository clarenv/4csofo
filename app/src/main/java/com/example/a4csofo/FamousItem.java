package com.example.a4csofo;

public class FamousItem {
    public String name;
    public String description;
    public double price;
    public int sales;

    public FamousItem() {}

    public FamousItem(String name, String description, double price, int sales) {
        this.name = name;
        this.description = description;
        this.price = price;
        this.sales = sales;
    }
}
